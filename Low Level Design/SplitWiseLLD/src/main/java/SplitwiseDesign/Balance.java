package SplitwiseDesign;

public class Balance {
    private double amountOwe;
    private double amountGiveBack;

    public double getAmountOwe() {
        return amountOwe;
    }

    public void setAmountOwe(double amountOwe) {
        this.amountOwe = amountOwe;
    }

    public double getAmountGiveBack() {
        return amountGiveBack;
    }

    public void setAmountGiveBack(double amountGiveBack) {
        this.amountGiveBack = amountGiveBack;
    }
}
